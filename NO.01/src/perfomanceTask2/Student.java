
public class Student {
	//PRIVATE USE OF VARIABLES
	private String name;
	private int roll_no;
	
	//Setters 
	
	//SET name
	public void Setname(String Name) {
		this.name = Name;
		} 
	
	//SET roll_no
	public void Setroll_no(int r) {
		this.roll_no = r;
		}
	
	//Getters
	//GET, RETURN THE NAME 
	public String Getname() {
		return this.name;
	}
	
	//GET, RETURN THE roll_no
	public int Getroll_no() {
		return this.roll_no;
	}
	
	
}
